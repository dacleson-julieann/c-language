// example 3: float and double Output

#include<stdio.h>
int main()
{
  float number1 = 13.5;
  double number2 = 12.4;

  printf("number1 = %f\n", number1);
  printf("number2 = %lf", number2);
  return 0;
}

/*
to printf float, we use %f format specifier. similarly, we use %lf
to print double values.
*/
